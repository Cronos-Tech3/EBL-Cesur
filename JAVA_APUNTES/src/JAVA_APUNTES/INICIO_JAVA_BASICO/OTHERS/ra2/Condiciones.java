package ra2;

public class Condiciones {
    public static void main(String[] args) {
        int num =3;
        if (num==0) {
            System.out.println("cuando numero sea 0");

        } else if (num==1){
            System.out.println("cuando el numero sea 1");

        } else if (num==2) {
            System.out.println("cuando el numero sea 2");
        } else {
            System.out.println("cuando no sea ni 1 ni 0 ni 2");

        }

    }
}
