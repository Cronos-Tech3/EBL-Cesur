package JAVA_APUNTES.BUCLES.Bucles_Curso_Youtube;

public class Ej1For {
    public static void main(String[] args) {
        //muestra los multiples de 5 de 0 a 100 con bucle for
        for (int i = 0; i<=100;i++) {
            if (i%5==0) {
                System.out.println(i);
            }
        }

    }
}
